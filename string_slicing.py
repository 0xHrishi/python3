#Script for string slicing 
#Allows the user to get the outpu based upon the index which stars from 0 
# String slicing [start:end:step]
#Reverse string slicing stars from negative -1

full_name="Hrishikesh Dilip Ghosalkar"

print(full_name)
#string slicing [start:end:step]

print(full_name[0:5])
print(full_name[0::2])
#reverse string
print(full_name[::-1])
print(full_name[-1:-10])
